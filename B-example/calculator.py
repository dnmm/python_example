NUM1 =int(input("ENTER THE VALUE 1:-"))
NUM2 =int(input("ENTER THE VALUE 2:-"))
OPR =input("ENTER THE OPRATION(+,_,*,/)")
if OPR == "+" :
    print(NUM1+ NUM2)
elif OPR == "-" :
    print(NUM1- NUM2)
elif OPR == "*" :
    print(NUM1* NUM2)
elif OPR == "/" :
    print(NUM1/ NUM2)
else:
    print("invalid no.....")            
    
